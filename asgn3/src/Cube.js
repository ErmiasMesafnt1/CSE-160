function appendTerrainCellQuad(out, y0, y1, y2, y3, ix, iz) {
  var x0 = ix;
  var x1 = ix + 1;
  var z0 = iz;
  var z1 = iz + 1;
  var ru = 4;
  var u00 = x0 * ru;
  var u10 = x1 * ru;
  var v0 = z0 * ru;
  var v1 = z1 * ru;
  function v(px, py, pz, u, v) {
    out.push(px, py, pz, u, v);
  }
  v(x0, y0, z0, u00, v0);
  v(x1, y1, z0, u10, v0);
  v(x1, y2, z1, u10, v1);
  v(x0, y0, z0, u00, v0);
  v(x1, y2, z1, u10, v1);
  v(x0, y3, z1, u00, v1);
}
